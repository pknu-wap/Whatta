import React, { useState } from 'react'
import { View, Dimensions, LayoutChangeEvent } from 'react-native'

export default function FullBleed({
  children,
  padH = 12,
  fill = false,
}: {
  children: React.ReactNode
  padH?: number
  fill?: boolean
}) {
  const [parentW, setParentW] = useState<number | null>(null)
  const screenW = Dimensions.get('window').width
  const side = parentW == null ? 0 : (screenW - parentW) / 2

  return (
    <View
      onLayout={(e: LayoutChangeEvent) =>
        setParentW(e.nativeEvent.layout.width)
      }
      style={{
        marginLeft: parentW == null ? 0 : -side,
        width: screenW,
        paddingHorizontal: padH,
        ...(fill ? { flex: 1 } : null),
      }}
    >
      {children}
    </View>
  )
}